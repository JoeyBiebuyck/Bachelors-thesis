def scaled_gaussian_bandit(n):
    if n > 1000:
        raise ValueError("No more than 1000 arms are supported")
    means_ = scaled_gaussian_means()[:n]
    vars_ = scaled_gaussian_variances()[:n]
    def reward_fn(normal_params):
        mu,var = normal_params
        return lambda: np.random.normal(mu, np.sqrt(var))
    arms = list(map(reward_fn, zip(means_, vars_)))
    return Bandit(arms)

